using System;

namespace Интерпретатор_машины_Тьюринга.Core
{
    /// <summary>
    /// Контракт глобальной шины автоматического обновления табличных данных.
    ///
    /// Назначение интерфейса (DIP):
    ///   • Окна (Мои курсы, Администрирование, Проверка работ и пр.) подписываются
    ///     на абстракцию IDataRefreshBus, а не на конкретный static-класс.
    ///   • Это даёт возможность подменить реализацию (тесты, мок-шина, доменное
    ///     разделение по типам сущностей) без изменения подписчиков.
    ///
    /// Назначение публикации событий:
    ///   • После любой успешной CRUD-операции (создание, изменение, удаление сущностей —
    ///     группа/курс/задание/решение/студент/преподаватель) шина рассылает
    ///     уведомление всем заинтересованным окнам.
    ///   • Источника два:
    ///       1) SignalR push «DataChanged» от сервера.
    ///       2) Локальный триггер из самого клиента после успешного REST-ответа.
    ///
    /// Параметры события:
    ///   • entity — "Group", "Course", "Assignment", "Submission", "Student",
    ///     "Teacher", "CourseGroups", "GradingPolicy".
    ///   • action — "Created", "Updated", "Deleted".
    ///   • id     — идентификатор затронутой сущности (если применимо), иначе 0.
    /// </summary>
    public interface IDataRefreshBus
    {
        event Action<string, string, int> Changed;
        void Raise(string entity, string action, int id = 0);
    }
}
